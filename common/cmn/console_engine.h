	class ConsoleEngine {
		static const int _num_keys=512;
		bool _keys_prev[_num_keys],
			_keys_curr[_num_keys],
			_keys_next[_num_keys];

		static const int _num_mouse_buttons=8;
		bool _mouse_buttons_prev[_num_mouse_buttons],
			_mouse_buttons_curr[_num_mouse_buttons],
			_mouse_buttons_next[_num_mouse_buttons];

		int _mouse_x_next=0, _mouse_y_next=0;
		float _mouse_x_curr=0, _mouse_y_curr=0;

		Font _font;

		struct inst_data_t {
			float pos[2];
			float ltrb[4];
			float col[3];
			float bg_col[3];
		};
		struct {
			sg_pipeline pip{};

			sg_sampler sampler{};

			sg_buffer vbuf{};

			int max_num=0;
			inst_data_t* instances=nullptr;

			sg_buffer ibuf{};
		} _raster_render;

		void updateRaster() {
			rst.resize(
				1+sapp_widthf()/_font.char_w,
				1+sapp_heightf()/_font.char_h
			);
		}

		void setupRasterRender() {
			sg_pipeline_desc pip_desc{};
			pip_desc.shader=sg_make_shader(raster_shader_desc(sg_query_backend()));
			pip_desc.layout.buffers[1].step_func=SG_VERTEXSTEP_PER_INSTANCE;
			pip_desc.layout.attrs[ATTR_raster_i_uv].format=SG_VERTEXFORMAT_FLOAT2;
			pip_desc.layout.attrs[ATTR_raster_i_uv].buffer_index=0;
			pip_desc.layout.attrs[ATTR_raster_inst_pos].format=SG_VERTEXFORMAT_FLOAT2;
			pip_desc.layout.attrs[ATTR_raster_inst_pos].buffer_index=1;
			pip_desc.layout.attrs[ATTR_raster_inst_ltrb].format=SG_VERTEXFORMAT_FLOAT4;
			pip_desc.layout.attrs[ATTR_raster_inst_ltrb].buffer_index=1;
			pip_desc.layout.attrs[ATTR_raster_inst_col].format=SG_VERTEXFORMAT_FLOAT3;
			pip_desc.layout.attrs[ATTR_raster_inst_col].buffer_index=1;
			pip_desc.layout.attrs[ATTR_raster_inst_bg_col].format=SG_VERTEXFORMAT_FLOAT3;
			pip_desc.layout.attrs[ATTR_raster_inst_bg_col].buffer_index=1;
			pip_desc.primitive_type=SG_PRIMITIVETYPE_TRIANGLE_STRIP;
			_raster_render.pip=sg_make_pipeline(pip_desc);

			sg_sampler_desc sampler_desc{};
			_raster_render.sampler=sg_make_sampler(sampler_desc);

			float vertexes[4][2]{{0, 0}, {1, 0}, {0, 1}, {1, 1}};
			sg_buffer_desc vbuf_desc{};
			vbuf_desc.data=SG_RANGE(vertexes);
			_raster_render.vbuf=sg_make_buffer(vbuf_desc);

			_raster_render.max_num=(1+1920/_font.char_w)*(1+1080/_font.char_h);
			_raster_render.instances=new inst_data_t[_raster_render.max_num];

			sg_buffer_desc ibuf_desc{};
			ibuf_desc.size=sizeof(inst_data_t)*_raster_render.max_num;
			ibuf_desc.usage.stream_update=true;
			_raster_render.ibuf=sg_make_buffer(ibuf_desc);
		}

		void renderRaster() {
			//display pass
			sg_pass pass{};
			pass.action.colors[0].load_action=SG_LOADACTION_CLEAR;
			pass.action.colors[0].clear_value={0, 0, 0, 1};
			pass.swapchain=sglue_swapchain();
			sg_begin_pass(pass);

			//update instance data
			float width_recip=1/sapp_widthf();
			float height_recip=1/sapp_heightf();
			float col_recip=1.f/255;
			for(int i=0; i<rst.getWidth(); i++) {
				for(int j=0; j<rst.getHeight(); j++) {
					int k=rst.ix(i, j);
					const auto& glyph=rst.glyphs[k];
					auto& inst_data=_raster_render.instances[k];
					inst_data.pos[0]=_font.char_w*width_recip*i;
					inst_data.pos[1]=_font.char_h*height_recip*j;
					_font.getRegion(glyph.c,
						inst_data.ltrb[0],
						inst_data.ltrb[1],
						inst_data.ltrb[2],
						inst_data.ltrb[3]
					);
					inst_data.col[0]=col_recip*glyph.r;
					inst_data.col[1]=col_recip*glyph.g;
					inst_data.col[2]=col_recip*glyph.b;
					inst_data.bg_col[0]=col_recip*glyph.br;
					inst_data.bg_col[1]=col_recip*glyph.bg;
					inst_data.bg_col[2]=col_recip*glyph.bb;
				}
			}
			sg_range range{};
			range.ptr=_raster_render.instances;
			range.size=sizeof(inst_data_t)*rst.getWidth()*rst.getHeight();
			sg_update_buffer(_raster_render.ibuf, range);

			sg_apply_pipeline(_raster_render.pip);

			sg_bindings bind{};
			bind.vertex_buffers[0]=_raster_render.vbuf;
			bind.vertex_buffers[1]=_raster_render.ibuf;
			bind.samplers[SMP_b_raster_smp]=_raster_render.sampler;
			bind.views[VIEW_b_raster_tex]=_font.tex;
			sg_apply_bindings(bind);

			p_vs_raster_t p_vs_raster{};
			p_vs_raster.u_num[0]=rst.getWidth();
			p_vs_raster.u_num[1]=rst.getHeight();
			sg_apply_uniforms(UB_p_vs_raster, SG_RANGE(p_vs_raster));

			sg_draw(0, 4, rst.getWidth()*rst.getHeight());

			sg_end_pass();

			sg_commit();
		}

	public:
		virtual bool user_create()=0;
		virtual bool user_update(float dt)=0;
		virtual bool user_raster()=0;
		virtual void user_input(const sapp_event*) {};
		virtual void user_destroy() {};

		Raster rst;

		std::string app_title="[untitled]";

		void init() {
			sg_desc desc{};
			sg_setup(desc);

			std::memset(_keys_prev, false, sizeof(bool)*_num_keys);
			std::memset(_keys_curr, false, sizeof(bool)*_num_keys);
			std::memset(_keys_next, false, sizeof(bool)*_num_keys);

			std::memset(_mouse_buttons_prev, false, sizeof(bool)*_num_mouse_buttons);
			std::memset(_mouse_buttons_curr, false, sizeof(bool)*_num_mouse_buttons);
			std::memset(_mouse_buttons_next, false, sizeof(bool)*_num_mouse_buttons);

			_font=Font::makeIBMBios8x8();

			updateRaster();

			setupRasterRender();

			if(!user_create()) sapp_request_quit();
		}

		void frame() {
			//update keys
			std::memcpy(_keys_prev, _keys_curr, sizeof(_keys_curr));
			std::memcpy(_keys_curr, _keys_next, sizeof(_keys_curr));

			//update mouse buttons
			std::memcpy(_mouse_buttons_prev, _mouse_buttons_curr, sizeof(_mouse_buttons_curr));
			std::memcpy(_mouse_buttons_curr, _mouse_buttons_next, sizeof(_mouse_buttons_curr));

			//update mouse pos
			_mouse_x_curr=_mouse_x_next/_font.char_w;
			_mouse_y_curr=_mouse_y_next/_font.char_h;

			float dt=sapp_frame_duration();

			//update title with fps
			{
				char buf[256];
				std::snprintf(
					buf,
					sizeof(buf),
					"%s - FPS: %d",
					app_title.c_str(),
					int(1/dt)
				);
				sapp_set_window_title(buf);
			}

			if(!user_update(dt)) sapp_request_quit();

			updateRaster();

			if(!user_raster()) sapp_request_quit();

			renderRaster();
		}

		void input(const sapp_event* e) {
			switch(e->type) {
				case SAPP_EVENTTYPE_KEY_DOWN:
					_keys_next[e->key_code]=true;
					break;
				case SAPP_EVENTTYPE_KEY_UP:
					_keys_next[e->key_code]=false;
					break;
				case SAPP_EVENTTYPE_MOUSE_DOWN:
					_mouse_buttons_next[e->mouse_button]=true;
					break;
				case SAPP_EVENTTYPE_MOUSE_UP:
					_mouse_buttons_next[e->mouse_button]=false;
					break;
				case SAPP_EVENTTYPE_MOUSE_MOVE: {
					float dpi=sapp_dpi_scale();
					_mouse_x_next=e->mouse_x/dpi;
					_mouse_y_next=e->mouse_y/dpi;
					break;
				}
				default: break;
			}

			user_input(e);
		}

		void cleanup() {
			user_destroy();

			delete[] _raster_render.instances;
			sg_shutdown();
		}

		struct ButtonState { bool pressed, held, released; };

		ButtonState GetKey(const sapp_keycode& k) const {
			bool curr=_keys_curr[k], prev=_keys_prev[k];
			return {curr&&!prev, curr, !curr&&prev};
		}

		ButtonState GetMouse(const sapp_mousebutton& m) const {
			bool curr=_mouse_buttons_curr[m], prev=_mouse_buttons_prev[m];
			return {curr&&!prev, curr, !curr&&prev};
		}

		int GetMouseX() const { return _mouse_x_curr; }
		int GetMouseY() const { return _mouse_y_curr; }
	};

