	class Font {
		static const std::uint64_t
			packed_graspee_8x8[],
			packed_ibmbios_8x8[],
			packed_adammil_10x10[],
			packed_adammil_12x12[];

		Font(const std::uint64_t* packed, int w, int h) {
			char_w=w, char_h=h;
			num_x=16, num_y=16;

			//unpack ints into image
			int width=num_x*char_w;
			int height=num_y*char_h;
			std::uint32_t* pixels=new std::uint32_t[width*height];
			for(int i=0; i<width; i++) {
				for(int j=0; j<height; j++) {
					int k=i+width*j;
					bool b=1&(packed[k/64]>>(k%64));
					pixels[k]=b?0xFFFFFFFF:0x00000000;
				}
			}

			sg_image_desc image_desc{};
			image_desc.width=num_x*char_w;
			image_desc.height=num_y*char_h;
			image_desc.data.mip_levels[0].ptr=pixels;
			image_desc.data.mip_levels[0].size=sizeof(std::uint32_t)*width*height;
			sg_image image=sg_make_image(image_desc);

			delete[] pixels;

			sg_view_desc view_desc{};
			view_desc.texture.image=image;
			tex=sg_make_view(view_desc);
		}

	public:

		sg_view tex{};

		int char_w=0, char_h=0;
		int num_x=1, num_y=1;

		Font() {}

		//region of texture given character
		void getRegion(char c, float& l, float& t, float& r, float& b) const {
			int i=c%num_x, j=c/num_x;

			float x1=1.f/num_x, y1=1.f/num_y;
			l=x1*i, t=y1*j;
			r=x1*(1+i), b=y1*(1+j);
		}

		static Font makeGraspee8x8() {
			return Font(packed_graspee_8x8, 8, 8);
		}

		static Font makeIBMBios8x8() {
			return Font(packed_ibmbios_8x8, 8, 8);
		}

		static Font makeAdamMil10x10() {
			return Font(packed_adammil_10x10, 10, 10);
		}

		static Font makeAdamMil12x12() {
			return Font(packed_adammil_12x12, 12, 12);
		}
	};

