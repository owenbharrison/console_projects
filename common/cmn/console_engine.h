	struct Glyph {
		char c=' ';
		unsigned char r=255, g=255, b=255;
		unsigned char br=0, bg=0, bb=0;
	};

	class Raster {
		int width=0, height=0;

	public:
		Glyph* glyphs=nullptr;

		Raster() {
			resize(1, 1);
		}

		//ro3: 1
		Raster(const Raster&)=delete;

		//ro3: 2
		~Raster() {
			delete[] glyphs;
		}

		//ro3: 3
		Raster& operator=(const Raster&)=delete;

		int getWidth() const { return width; }

		int getHeight() const { return height; }

		int ix(int i, int j) const {
			return i+width*j;
		}

		void resize(int w, int h) {
			//skip if superfluous
			if(w==width&&h==height) return;

			//reallocate new grids
			Glyph* new_glyphs=new Glyph[w*h];
			for(int i=0; i<w*h; i++) {
				new_glyphs[i]={' '};
			}

			//deallocate old
			delete[] glyphs;

			//assign size & ptrs
			width=w, height=h;
			glyphs=new_glyphs;
		}

		void clear(const Glyph& g) {
			for(int i=0; i<width*height; i++) {
				glyphs[i]=g;
			}
		}

		//this will be slow...
		void draw(int x, int y, const Glyph& g) {
			if(x<0||y<0||x>=width||y>=height) return;

			int k=ix(x, y);
			glyphs[k]=g;
		}

		void draw_f(float x, float y, const Glyph& g) {
			draw(.5f+x, .5f+y, g);
		}

		//bresenhams algorithm
		void draw_line(int x0, int y0, int x1, int y1, const Glyph& g) {
			int dx=x1-x0;
			if(dx<0) dx=-dx;
			int dy=y1-y0;
			if(dy<0) dy=-dy;

			int sx=(x0<x1)?1:-1;
			int sy=(y0<y1)?1:-1;

			int err=dx-dy;

			while(true) {
				draw(x0, y0, g);
				if(x0==x1&&y0==y1) break;

				int e2=2*err;
				if(e2>-dy) err-=dy, x0+=sx;
				if(e2<dx) err+=dx, y0+=sy;
			}
		}

		void draw_line_f(float x0, float y0, float x1, float y1, const Glyph& g) {
			draw_line(.5f+x0, .5f+y0, .5f+x1, .5f+y1, g);
		}

		void draw_triangle(int x0, int y0, int x1, int y1, int x2, int y2, const Glyph& g) {
			draw_line(x0, y0, x1, y1, g);
			draw_line(x1, y1, x2, y2, g);
			draw_line(x2, y2, x0, y0, g);
		}

		void draw_triangle_f(float x0, float y0, float x1, float y1, float x2, float y2, const Glyph& g) {
			draw_line_f(x0, y0, x1, y1, g);
			draw_line_f(x1, y1, x2, y2, g);
			draw_line_f(x2, y2, x0, y0, g);
		}

		void fill_flat_bottom(int x0, int y0, int x1, int y1, int x2, int y2, const Glyph& g) {
			float inv_m1=float(x1-x0)/(y1-y0);
			float inv_m2=float(x2-x0)/(y2-y0);

			float curx1=x0, curx2=x0;

			for(int y=y0; y<=y1; y++) {
				int sx=.5f+curx1, ex=.5f+curx2;
				if(sx>ex) std::swap(sx, ex);

				for(int x=sx; x<=ex; x++) draw(x, y, g);

				curx1+=inv_m1, curx2+=inv_m2;
			}
		}

		void fill_flat_top(int x0, int y0, int x1, int y1, int x2, int y2, const Glyph& g) {
			float inv_m1=float(x2-x0)/(y2-y0);
			float inv_m2=float(x2-x1)/(y2-y1);

			float curx1=x2, curx2=x2;

			for(int y=y2; y>=y0; y--) {
				int sx=.5f+curx1, ex=.5f+curx2;
				if(sx>ex) std::swap(sx, ex);

				for(int x=sx; x<=ex; x++) draw(x, y, g);

				curx1-=inv_m1, curx2-=inv_m2;
			}
		}

		void fill_triangle(int x0, int y0, int x1, int y1, int x2, int y2, const Glyph& g) {
			//sort by y
			if(y1<y0) std::swap(x0, x1), std::swap(y0, y1);
			if(y2<y0) std::swap(x0, x2), std::swap(y0, y2);
			if(y2<y1) std::swap(x1, x2), std::swap(y1, y2);

			if(y1==y2) {
				fill_flat_bottom(x0, y0, x1, y1, x2, y2, g);
			} else if(y0==y1) {
				fill_flat_top(x0, y0, x1, y1, x2, y2, g);
			} else {
				//split triangle
				float t=float(y1-y0)/(y2-y0);
				int x3=x0+t*(x2-x0), y3=y1;
				fill_flat_bottom(x0, y0, x1, y1, x3, y3, g);
				fill_flat_top(x1, y1, x3, y3, x2, y2, g);
			}
		}

		void fill_triangle_f(float x0, float y0, float x1, float y1, float x2, float y2, const Glyph& g) {
			fill_triangle(.5f+x0, .5f+y0, .5f+x1, .5f+y1, .5f+x2, .5f+y2, g);
		}

		//https://en.wikipedia.org/wiki/Midpoint_circle_algorithm
		void draw_circle(int xc, int yc, int rad, const Glyph& g) {
			int x=0, y=rad;
			int p=3-2*rad;
			if(!rad) return;

			while(y>=x) {
				draw(xc-x, yc-y, g);
				draw(xc-y, yc-x, g);
				draw(xc+y, yc-x, g);
				draw(xc+x, yc-y, g);
				draw(xc-x, yc+y, g);
				draw(xc-y, yc+x, g);
				draw(xc+y, yc+x, g);
				draw(xc+x, yc+y, g);
				if(p<0) p+=4*(x++)+6;
				else p+=4*(x++-y--)+10;
			}
		}

		void draw_circle_f(float xc, float yc, float rad, const Glyph& g) {
			draw_circle(.5f+xc, .5f+yc, .5f+rad, g);
		}

		//https://en.wikipedia.org/wiki/Midpoint_circle_algorithm
		void fill_circle(int xc, int yc, int rad, const Glyph& g) {
			int x=0, y=rad;
			int p=3-2*rad;
			if(!rad) return;

			auto drawline=[&] (int sx, int ex, int ny) {
				for(int i=sx; i<=ex; i++) draw(i, ny, g);
			};

			while(y>=x) {
				drawline(xc-x, xc+x, yc-y);
				drawline(xc-y, xc+y, yc-x);
				drawline(xc-x, xc+x, yc+y);
				drawline(xc-y, xc+y, yc+x);
				if(p<0) p+=4*x+++6;
				else p+=4*(x++-y--)+10;
			}
		}

		void fill_circle_f(float xc, float yc, float rad, const Glyph& g) {
			fill_circle(.5f+xc, .5f+yc, .5f+rad, g);
		}

		void draw_rect(int x, int y, int w, int h, const Glyph& g) {
			for(int i=0; i<w; i++) {
				draw(x+i, y, g);
				draw(x+i, y+h-1, g);
			}
			for(int j=1; j<h-1; j++) {
				draw(x, y+j, g);
				draw(x+w-1, y+j, g);
			}
		}

		void draw_rect_f(float x, float y, float w, float h, const Glyph& g) {
			draw_rect(.5f+x, .5f+y, .5f+w, .5f+h, g);
		}

		void fill_rect(int x, int y, int w, int h, const Glyph& g) {
			for(int i=0; i<w; i++) {
				for(int j=0; j<h; j++) {
					draw(x+i, y+j, g);
				}
			}
		}

		void fill_rect_f(float x, float y, float w, float h, const Glyph& g) {
			fill_rect(.5f+x, .5f+y, .5f+w, .5f+h, g);
		}

		//glyph char unused
		void draw_string(int x, int y, const std::string& str, const Glyph& g) {
			int ox=0, oy=0;
			for(const auto& c:str) {
				//newline
				if(c=='\n') ox=0, oy++;
				//tabsize=2
				else if(c=='\t') ox+=2;
				else if(c>=32&&c<=127) {
					draw(x+ox, y+oy, {c, g.r, g.g, g.b, g.br, g.bg, g.bb});
					ox++;
				}
			}
		}

		//glyph char unused
		void draw_string_f(float x, float y, const std::string& str, const Glyph& g) {
			draw_string(.5f+x, .5f+y, str, g);
		}
	};

